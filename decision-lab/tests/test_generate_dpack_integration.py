def _test_python_resolves(dpack_info: tuple[str, str, Path]) -> None:
    pkg_mgr, image_name, _ = dpack_info
    exit_code, stdout, stderr = _run_in_container(image_name, ["which", "python"])
    python_path: str = stdout.strip()

    assert exit_code == 0, f"which python failed: {stderr}"
    assert python_path != "", "python not found on PATH"
    if pkg_mgr in EXPECTED_PYTHON_PATHS:
        assert python_path == EXPECTED_PYTHON_PATHS[pkg_mgr], (
            f"{pkg_mgr}: expected {EXPECTED_PYTHON_PATHS[pkg_mgr]}, got {python_path}"
        )


def _test_modal_importable(dpack_info: tuple[str, str, Path]) -> None:
    _, image_name, _ = dpack_info
    exit_code, stdout, stderr = _run_in_container(
        image_name, ["python", "-c", "import modal; print(modal.__version__)"]
    )
    assert exit_code == 0, f"import modal failed: {stderr}"
    assert stdout.strip() != "", "modal version empty"


def _test_deploy_hook(dpack_info: tuple[str, str, Path]) -> None:
    _, image_name, dpack_path = dpack_info
    container_name: str = f"test-hook-{uuid.uuid4().hex[:8]}"
    work_dir: Path = Path(f"/tmp/test-work-{container_name}")
    work_dir.mkdir(parents=True, exist_ok=True)
    (work_dir / "_opencode_logs").mkdir(exist_ok=True)

    hooks_dir: Path = work_dir / "_hooks"
    hooks_dir.mkdir()
    hook_dst: Path = hooks_dir / "deploy_modal.sh"
    hook_dst.write_text((dpack_path / "deploy_modal.sh").read_text())
    hook_dst.chmod(0o755)

    try:
        start_container(image_name, str(work_dir), container_name)

        exit_code, stdout, stderr = exec_command(
            container_name,
            ["bash", "-c", "DLAB_RUN_MODAL_TOOL_LOCALLY=1 /workspace/_hooks/deploy_modal.sh"],
        )
        assert exit_code == 0, f"Hook failed: {stderr}\n{stdout}"
        assert "Skipping Modal deployment" in stdout

        exit_code, stdout, stderr = exec_command(container_name, ["modal", "--help"])
        assert exit_code == 0, f"modal CLI not found: {stderr}"
    finally:
        stop_container(container_name)
        subprocess.run(["rm", "-rf", str(work_dir)], capture_output=True)


