"""
Integration tests for generate_dpack with Docker builds.

Tests that decision-packs generated by generate_dpack() for each package manager
with modal_integration=True produce working Docker images where:
1. The modal pre-run hook runs without conda activation issues
2. `python` resolves to the correct managed python
3. `modal` is importable

Run all:
    pytest tests/test_generate_dpack_integration.py -v -s

Run one package manager:
    pytest tests/test_generate_dpack_integration.py::TestConda -v -s
    pytest tests/test_generate_dpack_integration.py::TestPip -v -s
    pytest tests/test_generate_dpack_integration.py::TestUv -v -s
    pytest tests/test_generate_dpack_integration.py::TestPixi -v -s
"""

import subprocess
import tempfile
import uuid
from pathlib import Path
from typing import Any, Generator

import pytest

from dlab.create_dpack import generate_dpack
from dlab.docker import (
    build_image,
    exec_command,
    start_container,
    stop_container,
)


def _remove_image(name: str) -> None:
    """Remove a Docker image, ignoring errors if it doesn't exist."""
    subprocess.run(["docker", "rmi", "-f", name], capture_output=True)


EXPECTED_PYTHON_PATHS: dict[str, str] = {
    "conda": "/opt/conda/bin/python",
    "pip": "/usr/local/bin/python",
    "uv": "/usr/local/bin/python",
    "pixi": "/opt/pixi/.pixi/envs/default/bin/python",
}


def _run_in_container(image_name: str, command: list[str]) -> tuple[int, str, str]:
    """Start a container, run a command, stop it, return (exit_code, stdout, stderr)."""
    container_name: str = f"test-{uuid.uuid4().hex[:8]}"
    work_dir: Path = Path(f"/tmp/test-work-{container_name}")
    work_dir.mkdir(parents=True, exist_ok=True)
    (work_dir / "_opencode_logs").mkdir(exist_ok=True)

    try:
        start_container(image_name, str(work_dir), container_name)
        return exec_command(container_name, command)
    finally:
        stop_container(container_name)
        subprocess.run(["rm", "-rf", str(work_dir)], capture_output=True)


def _build_dpack_image(pkg_mgr: str) -> tuple[str, str, Path]:
    """Generate a dpack and build its Docker image. Returns (pkg_mgr, image_name, dpack_path)."""
    tmp_dir: Path = Path(tempfile.mkdtemp(prefix=f"dlab-inttest-{pkg_mgr}-"))
    name: str = f"inttest-{pkg_mgr}-{uuid.uuid4().hex[:6]}"
    image_name: str = f"dlab-{name}"

    config: dict[str, Any] = {
        "name": name,
        "package_manager": pkg_mgr,
        "modal_integration": True,
        "requires_data": False,
        "requires_prompt": False,
        "skeletons": {"tools": True},
    }

    dpack_path: Path = generate_dpack(tmp_dir, config)
    build_image(str(dpack_path), image_name, "latest")
    return pkg_mgr, image_name, dpack_path


def _cleanup_image(image_name: str) -> None:
    """Remove both wrapper and base images."""
    _remove_image(image_name)
    _remove_image(f"{image_name}-base")


