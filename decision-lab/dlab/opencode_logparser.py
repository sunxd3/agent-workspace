"""
Canonical log parser for OpenCode NDJSON output.

This is the single source of truth for parsing OpenCode log files in dlab.
All other modules (timeline.py, tui/log_watcher.py, tui/models.py) import
from here instead of doing their own JSON parsing.

OpenCode logs are NDJSON (one JSON object per line) with stderr mixed in
because dlab runs opencode with `2>&1 | tee`. Lines can be:
- JSON events (OpenCode step/text/tool events)
- Raw text (stderr from startup, database migrations, etc.)
- [STDERR] prefixed lines (from some logging paths)

Event types (from OpenCode source: packages/opencode/src/session/message-v2.ts):
- step_start: Agent step begins (part: snapshot?)
- step_finish: Agent step ends (part: reason, cost, tokens)
- text: Agent text output (part: text, time?, metadata?)
- tool_use: Tool invocation (part: tool, callID, state)
- error: Session error (error: {name, data: {message, statusCode}})
- reasoning: Extended thinking (part: text, time)

Synthetic types (generated by this parser for non-JSON content):
- raw_text: Non-JSON lines (stderr, startup messages)
- additional_output: JSON without type/timestamp (tool output fragments)

Tool state machine: pending → running → completed | error
Step finish reasons: stop, tool-calls, error, max-tokens, unknown
"""

import json
from dataclasses import dataclass, field
from datetime import datetime
from pathlib import Path
from typing import Any, Iterator


def ms_to_datetime(timestamp_ms: int) -> datetime:
    """
    Convert millisecond timestamp to datetime.

    Parameters
    ----------
    timestamp_ms : int
        Timestamp in milliseconds since epoch.

    Returns
    -------
    datetime
        Local datetime.
    """
    return datetime.fromtimestamp(timestamp_ms / 1000)


@dataclass
class LogEvent:
    """
    A single parsed log event.

    Attributes
    ----------
    event_type : str
        Event type: step_start, step_finish, text, tool_use, error,
        raw_text, additional_output, reasoning.
    timestamp : int | None
        Milliseconds since epoch. None for raw_text/additional_output.
    session_id : str
        OpenCode session ID (empty for synthetic events).
    part : dict[str, Any]
        Event-specific data (the "part" field from the JSON line).
    raw : dict[str, Any]
        Full raw JSON line (empty dict for raw_text).
    """

    event_type: str
    timestamp: int | None
    session_id: str
    part: dict[str, Any]
    raw: dict[str, Any]


def parse_line(line: str) -> LogEvent | None:
    """
    Parse a single log line into a LogEvent.

    Parameters
    ----------
    line : str
        Raw line from log file.

    Returns
    -------
    LogEvent | None
        Parsed event, or None for empty lines.
    """
    line = line.strip()
    if not line:
        return None

    # [STDERR] prefixed lines → raw_text
    if line.startswith("[STDERR]"):
        return LogEvent(
            event_type="raw_text",
            timestamp=None,
            session_id="",
            part={"text": line},
            raw={},
        )

    # Try JSON parsing
    if line.startswith("{"):
        try:
            data: dict[str, Any] = json.loads(line)
        except json.JSONDecodeError:
            # Looks like JSON but isn't → raw_text
            return LogEvent(
                event_type="raw_text",
                timestamp=None,
                session_id="",
                part={"text": line},
                raw={},
            )

        event_type: str | None = data.get("type")
        timestamp: int | None = data.get("timestamp")

        if event_type and timestamp is not None:
            # Normal OpenCode event
            return LogEvent(
                event_type=event_type,
                timestamp=timestamp,
                session_id=data.get("sessionID", ""),
                part=data.get("part", {}),
                raw=data,
            )
        else:
            # JSON but missing type/timestamp → additional_output
            return LogEvent(
                event_type="additional_output",
                timestamp=None,
                session_id="",
                part={"raw_data": data},
                raw=data,
            )

    # Plain text → raw_text
    return LogEvent(
        event_type="raw_text",
        timestamp=None,
        session_id="",
        part={"text": line},
        raw={},
    )


def parse_log_file(path: str | Path) -> list[LogEvent]:
    """
    Parse a log file into a list of events.

    Groups consecutive raw_text events into single events.

    Parameters
    ----------
    path : str | Path
        Path to the log file.

    Returns
    -------
    list[LogEvent]
        Parsed events in file order.
    """
    log_path: Path = Path(path)
    if not log_path.exists():
        return []

    events: list[LogEvent] = []
    raw_text_lines: list[str] = []

    def flush_raw_text() -> None:
        if raw_text_lines:
            events.append(LogEvent(
                event_type="raw_text",
                timestamp=None,
                session_id="",
                part={"text": "\n".join(raw_text_lines)},
                raw={},
            ))
            raw_text_lines.clear()

    with open(log_path, "r") as f:
        for line in f:
            event: LogEvent | None = parse_line(line)
            if event is None:
                continue

            if event.event_type == "raw_text":
                raw_text_lines.append(event.part.get("text", ""))
            else:
                flush_raw_text()
                events.append(event)

    flush_raw_text()
    return events


def iter_log_events(path: str | Path) -> Iterator[LogEvent]:
    """
    Iterate over log events from a file (generator, no grouping).

    Parameters
    ----------
    path : str | Path
        Path to the log file.

    Yields
    ------
    LogEvent
        Each parsed event.
    """
    log_path: Path = Path(path)
    if not log_path.exists():
        return

    with open(log_path, "r") as f:
        for line in f:
            event: LogEvent | None = parse_line(line)
            if event is not None:
                yield event


def is_log_complete(events: list[LogEvent]) -> bool:
    """
    Check if a list of events represents a completed run.

    A log is complete if:
    - Its last step_finish event has reason "stop" or "error", OR
    - It contains an "error" event (job crashed/terminated)

    Parameters
    ----------
    events : list[LogEvent]
        Parsed events from a log file.

    Returns
    -------
    bool
        True if the log shows a completed run.
    """
    if not events:
        return False

    last_step_finish: LogEvent | None = None
    has_error: bool = False

    for event in events:
        if event.event_type == "step_finish":
            last_step_finish = event
        elif event.event_type == "error":
            has_error = True

    if has_error:
        return True

    if last_step_finish is None:
        return False

    reason: str = last_step_finish.part.get("reason", "")
    return reason in ("stop", "error")


def is_log_file_complete(path: str | Path) -> bool:
    """
    Check if a log file represents a completed run.

    Convenience wrapper that parses the file and checks completion.

    Parameters
    ----------
    path : str | Path
        Path to the log file.

    Returns
    -------
    bool
        True if the log shows a completed run.
    """
    return is_log_complete(parse_log_file(path))


