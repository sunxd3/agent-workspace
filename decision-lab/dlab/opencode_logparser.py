def get_tool_name(event: LogEvent) -> str | None:
    """Get the tool name from a tool_use event, or None."""
    if event.event_type != "tool_use":
        return None
    return event.part.get("tool")


def get_tool_status(event: LogEvent) -> str | None:
    """Get the tool status (pending/running/completed/error), or None."""
    if event.event_type != "tool_use":
        return None
    return event.part.get("state", {}).get("status")


def get_tool_input(event: LogEvent) -> dict[str, Any] | None:
    """Get the tool input dict, or None."""
    if event.event_type != "tool_use":
        return None
    return event.part.get("state", {}).get("input")


def get_tool_output(event: LogEvent) -> str | None:
    """Get the tool output string, or None."""
    if event.event_type != "tool_use":
        return None
    return event.part.get("state", {}).get("output")


def get_tool_error(event: LogEvent) -> str | None:
    """Get the tool error string, or None."""
    if event.event_type != "tool_use":
        return None
    return event.part.get("state", {}).get("error")


def get_tool_time(event: LogEvent) -> tuple[int | None, int | None]:
    """Get (start_ms, end_ms) from a tool_use event."""
    if event.event_type != "tool_use":
        return None, None
    time_data: dict[str, Any] = event.part.get("state", {}).get("time", {})
    return time_data.get("start"), time_data.get("end")


def get_step_reason(event: LogEvent) -> str | None:
    """Get the step finish reason, or None."""
    if event.event_type != "step_finish":
        return None
    return event.part.get("reason")


def get_step_cost(event: LogEvent) -> float | None:
    """Get the step cost, or None."""
    if event.event_type != "step_finish":
        return None
    cost: float = event.part.get("cost", 0.0)
    return cost if cost else None


def get_step_tokens(event: LogEvent) -> dict[str, Any] | None:
    """
    Get the token usage dict from a step_finish event.

    Returns dict with: total, input, output, reasoning, cache.read, cache.write
    """
    if event.event_type != "step_finish":
        return None
    tokens: dict[str, Any] = event.part.get("tokens", {})
    return tokens if tokens else None


def get_text(event: LogEvent) -> str | None:
    """Get the text content from a text or raw_text event, or None."""
    if event.event_type in ("text", "raw_text"):
        return event.part.get("text")
    return None


def get_dlab_start_model(events: list[LogEvent]) -> str | None:
    """
    Extract model from the first dlab_start event, or None for older logs.

    Parameters
    ----------
    events : list[LogEvent]
        Parsed events from a log file.

    Returns
    -------
    str | None
        Model string (e.g. "anthropic/claude-sonnet-4-5") or None.
    """
    for event in events:
        if event.event_type == "dlab_start":
            return event.part.get("model") or event.raw.get("model")
        # Only check the first few events — dlab_start is always first
        if event.timestamp is not None:
            break
    return None


