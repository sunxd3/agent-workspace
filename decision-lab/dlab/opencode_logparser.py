@dataclass
class SessionNode:
    """
    A node in the session execution graph.

    Attributes
    ----------
    name : str
        Node name (e.g. "main", "instance-1", "consolidator").
    log_path : Path
        Path to the .log file.
    events : list[LogEvent]
        Parsed events from this log.
    children : list[SessionNode]
        Child nodes (parallel instances + consolidator).
    parent_event_index : int | None
        Index in parent's events where this node was spawned.
    agent_name : str
        Agent name (e.g. "data-preparer", "modeler", "main").
    is_consolidator : bool
        Whether this is a consolidator node.
    model : str | None
        Model used (from dlab_start event, or None for older logs).
    """

    name: str
    log_path: Path
    events: list[LogEvent] = field(default_factory=list)
    children: list["SessionNode"] = field(default_factory=list)
    parent_event_index: int | None = None
    agent_name: str = ""
    is_consolidator: bool = False
    model: str | None = None

    def __repr__(self) -> str:
        return self._repr_tree()

    def _repr_tree(self) -> str:
        """Render the session graph as a timeline showing main events between subagent calls."""
        if not self.children:
            # Leaf node or no parallel agents
            n: int = len(self.events)
            model_str: str = f" ({self.model})" if self.model else ""
            complete: str = "✓" if is_log_complete(self.events) else "…"
            return f"{self.name} [{n} events{model_str}] {complete}"

        lines: list[str] = []
        model_str = f" ({self.model})" if self.model else ""
        complete = "✓" if is_log_complete(self.events) else "…"
        lines.append(f"{self.name}{model_str} {complete}")

        # Group children by parent_event_index to show them at the right point
        spawn_points: dict[int | None, list["SessionNode"]] = {}
        for child in self.children:
            idx: int | None = child.parent_event_index
            if idx not in spawn_points:
                spawn_points[idx] = []
            spawn_points[idx].append(child)

        # Walk through main events, inserting children at spawn points
        prev_idx: int = 0
        sorted_indices: list[int] = sorted(i for i in spawn_points if i is not None)

        for spawn_idx in sorted_indices:
            # Count main events before this spawn
            events_before: int = spawn_idx - prev_idx
            if events_before > 0:
                lines.append(f"│ ... {events_before} events ...")

            # Show spawned children
            children: list["SessionNode"] = spawn_points[spawn_idx]
            for i, child in enumerate(children):
                is_last_child: bool = i == len(children) - 1
                connector: str = "└── " if is_last_child else "├── "
                agent_prefix: str = f"{child.agent_name}/" if child.agent_name != child.name else ""
                n_events: int = len(child.events)
                child_model: str = f" ({child.model})" if child.model else ""
                child_complete: str = "✓" if is_log_complete(child.events) else "…"
                lines.append(f"{connector}{agent_prefix}{child.name} [{n_events} events{child_model}] {child_complete}")

            prev_idx = spawn_idx + 1

        # Events after last spawn
        remaining: int = len(self.events) - prev_idx
        if remaining > 0:
            lines.append(f"│ ... {remaining} events ...")

        return "\n".join(lines)


def build_session_graph(logs_dir: str | Path) -> SessionNode | None:
    """
    Build a session execution graph from an _opencode_logs directory.

    The root node is main.log. Children are parallel agent instances
    and consolidators, linked by matching parallel-agents tool calls
    to their subfolder logs.

    Parameters
    ----------
    logs_dir : str | Path
        Path to the _opencode_logs directory.

    Returns
    -------
    SessionNode | None
        Root node of the session graph, or None if main.log doesn't exist.
    """
    logs_path: Path = Path(logs_dir)
    main_log: Path = logs_path / "main.log"

    if not main_log.exists():
        return None

    main_events: list[LogEvent] = parse_log_file(main_log)
    root: SessionNode = SessionNode(
        name="main",
        log_path=main_log,
        events=main_events,
        agent_name="main",
        model=get_dlab_start_model(main_events),
    )

    # Find all parallel-agents tool calls in main log
    for event_idx, event in enumerate(main_events):
        if event.event_type != "tool_use":
            continue
        if event.part.get("tool") != "parallel-agents":
            continue
        state: dict[str, Any] = event.part.get("state", {})
        if state.get("status") != "completed":
            continue

        input_data: dict[str, Any] = state.get("input", {})
        agent_name: str = input_data.get("agent", "")
        if not agent_name:
            continue

        # Find matching subfolder
        run_dir: Path | None = _find_parallel_run_dir(
            logs_path, agent_name, state.get("time", {}),
        )
        if run_dir is None:
            continue

        # Parse instance logs
        instance_logs: list[Path] = sorted(run_dir.glob("instance-*.log"))
        for instance_log in instance_logs:
            instance_events: list[LogEvent] = parse_log_file(instance_log)
            child: SessionNode = SessionNode(
                name=instance_log.stem,
                log_path=instance_log,
                events=instance_events,
                parent_event_index=event_idx,
                agent_name=agent_name,
                model=get_dlab_start_model(instance_events),
            )
            root.children.append(child)

        # Parse consolidator log if present
        consolidator_log: Path = run_dir / "consolidator.log"
        if consolidator_log.exists():
            cons_events: list[LogEvent] = parse_log_file(consolidator_log)
            cons_node: SessionNode = SessionNode(
                name="consolidator",
                log_path=consolidator_log,
                events=cons_events,
                parent_event_index=event_idx,
                agent_name=agent_name,
                is_consolidator=True,
                model=get_dlab_start_model(cons_events),
            )
            root.children.append(cons_node)

    return root


def _find_parallel_run_dir(
    logs_dir: Path,
    agent_name: str,
    time_data: dict[str, Any],
) -> Path | None:
    """
    Find the parallel run subfolder matching an agent name.

    Matches by agent name prefix. If multiple matches, picks the one
    with closest timestamp.

    Parameters
    ----------
    logs_dir : Path
        The _opencode_logs directory.
    agent_name : str
        Agent name from the parallel-agents tool call.
    time_data : dict[str, Any]
        Time data from the tool call state (has "start" key).

    Returns
    -------
    Path | None
        Path to the matching run directory, or None.
    """
    prefix: str = f"{agent_name}-parallel-run-"
    candidates: list[Path] = [
        d for d in logs_dir.iterdir()
        if d.is_dir() and d.name.startswith(prefix)
    ]

    if not candidates:
        return None

    if len(candidates) == 1:
        return candidates[0]

    # Multiple matches — pick closest timestamp
    tool_start: int = time_data.get("start", 0)
    if not tool_start:
        return candidates[0]

    def ts_distance(d: Path) -> int:
        try:
            folder_ts: int = int(d.name[len(prefix):])
            return abs(folder_ts - tool_start)
        except ValueError:
            return 10**18

    return min(candidates, key=ts_distance)
